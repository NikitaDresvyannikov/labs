def task5():
    pr = 1
    cnt = 0
    while cnt != 4:
        x = float(input('Введите отрицательное число\n'))
        if x < 0:
            pr *= x
            cnt += 1
        if x > 0:
            print('Ошибка!\t')
        if cnt == 4:
            if pr < 0:
                print('Произведение отрицательно\n', pr)
            if pr > 0:
                print('Произведение положительно\n', pr)

